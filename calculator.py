def add(a,b):
    return a+b
def subtract(a,b):
    return a-b
def multiply(a,b):
    return a*b
def division(a,b):
    return a/b
def modulus(a,b):
    return a%b
print("Addition:",add(9,6))
print("Subtraction:",subtract(20,5))
print("Multiplication:",multiply(19,4))
print("Division:",division(30,10))
print("Modulus:",modulus(12,6))